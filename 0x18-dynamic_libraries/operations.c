#include <stdio.h>

/**
 * add - fuction that add 2 number
 * @a: first number
 * @b: second number
 * Return: value of the operation
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - fuction that sub 2 number
 * @a: first number
 * @b: second number
 * Return: value of the operation
 */

int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - fuction that multiply 2 number
 * @a: first number
 * @b: second number
 * Return: value of the operation
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - fuction that divide 2 number
 * @a: first number
 * @b: second number
 * Return: value of the operation
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - fuction that give modulo of 2 number
 * @a: first number
 * @b: second number
 * Return: value of the operation
 */

int mod(int a, int b)
{
	return (a % b); 
}
